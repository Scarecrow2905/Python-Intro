name = "tommy etternavn"

print(len(name)) #Prints the length/number of characters
print(name.find("t")) #Prints the position of the letter in name
print(name.capitalize()) #Prints so the string is capitalized
print(name.upper()) #Prints in uppercase
print(name.lower()) #Prints in lowercase
print(name.isdigit()) #Check if its number /False
print(name.isalpha()) # Checks to see if its alphabetical letters
print(name.count("o")) #Counts and returns the number
print(name.replace("o", "a"))
print(name*3)
