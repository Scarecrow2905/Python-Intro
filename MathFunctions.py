import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi)) #Rounds the number to the closest round number. In this case: 3
print(math.ceil(pi)) #Short for ceiling, rounds the number up. In this case: 4
print(math.floor(pi)) # Same as ceil, just down instead. So in this case: 3
print(abs(pi)) #Absolute value: 3,14
print(pow(pi,2)) #(base,exponent) pow will raise the base number to a power
print(math.sqrt(420)) #SquareRoot
print(max(x,y,z)) #Finds the max value of these three variables
print(min(x,y,z)) #Finds the min value